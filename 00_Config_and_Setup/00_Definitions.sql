    TOPIC: DDL vs DML

    1. DDL (Data Definition Language):
       - DEFINES the structure (Schema).
       - Examples: CREATE table, ALTER column, DROP table.
       - "Building the house."

    2. DML (Data Manipulation Language):
       - MANIPULATES the data inside.
       - Examples: INSERT data, UPDATE values, DELETE rows.
       - "Moving the furniture."
